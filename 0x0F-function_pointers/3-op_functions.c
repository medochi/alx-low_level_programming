#include "3-calc.h"
/**
 * op_add - add
 *
 * ob_sub - sub
 *
 * ob_mul - mul
 *
 * op_div - div 
 *
 * op_mod - mod
 *
 * return : int
 *
 */


int op_add(int a, int b)
{
	return (a+b);
}
int op_sub(int a, int b)
{
	return (a-b);
}
int op_mul(int a, int b)
{
	return (a*b);

}
int op_div(int a, int b)
{
	return (a/b);
}
int op_mod(int a, int b)
{
	return (a%b);
}
